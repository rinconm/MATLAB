%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% Fundamental discrete functions and their plots
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
close all
clear all
clc
format
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Parameters
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Specify sample range
n=-10:10;
% The Kronecker Delta Function
delta=(n==0);
% The Unit Step Function
u_step=(n>=0);
% The Ramp Sequence
ramp=n.*(n>=0);
% One-Sided Exponential a^n*u[n]
a=1.2;
xn=(a).^n.*(n>=0);

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Function
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Plot delta[n]
subplot(221), stem(n, delta, 'filled')
xlabel('n'), title('delta[n]')
% Plot u[n]
subplot(222), stem(n, u_step, 'filled')
xlabel('n'), title('u[n]')
% Plot ramp[n]
subplot(223), stem(n, ramp, 'filled');grid
xlabel('n'), title('ramp[n]')
% Plot one-sided exponential
subplot(224), stem(n, xn, 'filled');grid
xlabel('n'), title({'a^n*u[n]',[' with a = ' num2str(a)]})

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% Discrete functions and their plots
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
close all
clear all
clc
format

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Parameters
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Specify sample range
n=-10:10;
% A_1*delta[n-n0_1]+A_2*delta[n-n0_2]+A_3*delta[n-n0_3]
A=[2 1 5];
n01=[0, 1, 3];
deltas=A(1)*((n-n01(1))==0)+A(2)*((n-n01(2))==0)+A(3)*((n-n01(3))==0);
% B*u[n-n0]
B=5;
n02=2;
u_step=B*((n-n02)>=0);
% C*a^(n-n0)*u[n-n0]
C=1;
a=1.5;
n03=-3;
xn=C*(a).^(n-n03).*((n-n03)>=0);

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Function
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Plot A_1*delta[n-n0_1]+A_2*delta[n-n0_2]+A_3*delta[n-n0_3]
subplot(221), stem(n, deltas, 'filled');grid
xlabel('n'), title({'A_1*delta[n-n0_1]+A_2*delta[n-n0_2]+A_3*delta[n-n0_3]',['with A_1, A_2, A_3 = ' num2str(A) ' and n0_1, n0_2, n0_3 = ' num2str(n01)]})
% Plot B*u[n-n0]
subplot(222), stem(n, u_step, 'filled')
xlabel('n'), title({'B*u[n-n0]',['with B = ' num2str(B) ' and n0 = ' num2str(n02)]})
% Plot C*a^(n-n0)*u[n-n0]
subplot(223), stem(n, xn, 'filled');grid
xlabel('n'), title({'C*a^(n-n0)*u[n-n0]',['with C = ' num2str(C) ', a = ' num2str(a) ', and n0 = ' num2str(n03)]}, 'Interpreter', 'none')
